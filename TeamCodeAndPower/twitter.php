          <h4>Reporting Abusive Users / Blocking Mean People</h4>

          

            <p>Twitter allows users to report and block offensive/abusive content in just a few clicks. All tweets contain a drop-down menu that includes six options:</p>

              <ul>
                 <li>Copy link to tweet</li>
                 <li>Embed tweet</li>
                 <li>Mute the author of the tweet</li>
                 <li>Block the author of the tweet</li>
                 <li>Report tweet</li>
                 <li>"I don't like this tweet"</li>

              </ul>

            <p>Upon clicking the “report” button, users are presented with three options:</p>

              <ul>
                <li>"I'm not interested in this tweet"</li>
                <li>"It's spam"</li>
                <li>"It's abusive or harmful"</li>
              </ul>

            <p>Selecting the third option initiates an investigation process that can result in the suspension of the user from Twitter.</p>

            <hr>

                <!-- My Score for Reporting: 5/5 -->

          <h4>Features Designed to Prevent Abuse</h4>

            <p>Twitter does does not currently utilize sophisticated features that prevent users from posting hateful/abusive content. However, users can limit the chances they will encounter harmful material by using Twitter’s mute feature. Users can mute other users; words or phrases; and hashtags. In this way, users can pre-curate the type of material that appears on their timeline and in their searches in order to minimize the chance of encountering offensive content. Such features, however, do not change the fact that harmful content may still exist on Twitter.</p>

            <hr>

                <!-- My Score for Preventative Measures: 3/5 -->

          <h4>User Agency</h4>

            <p>Twitter affords users substantial agency. Other than an email address, no personal information is required in order to register for an account. Further, users may keep their accounts completely anonymous. This allows users to move throughout the platform as they wish without other users knowing who they are. Such anonymity, of course, has benefits and drawbacks. Chief among those drawbacks is that some users feel empowered to post hateful content without fear of consequences.</p>

            <hr>


